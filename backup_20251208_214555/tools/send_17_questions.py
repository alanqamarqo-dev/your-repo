results = []

for i, q in enumerate(questions, start=1):
    payload = {"message": q}
    try:
        r = requests.post(f"{BASE}/chat", json=payload, timeout=60)
        data = r.json() if r.headers.get('Content-Type','').startswith('application/json') else {"raw": r.text}
        results.append({"q_index": i, "question": q, "status_code": r.status_code, "response": data})
        print(f"[{i}] status={r.status_code}")
    except Exception as e:
        results.append({"q_index": i, "question": q, "error": str(e)})
        print(f"[{i}] ERROR: {e}")

with open(OUT, 'w', encoding='utf-8') as fh:
    json.dump(results, fh, ensure_ascii=False, indent=2)

print('\nFinished. Results saved to', OUT)
