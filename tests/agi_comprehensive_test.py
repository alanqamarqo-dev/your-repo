async def run_suite():
    print("\n🧪 [AGI] Starting Comprehensive Scientific Test Suite")
    print("====================================================")
    
    # 1. Init
    print("🧠 Initializing AGI System...")
    engine_registry = {}
    bootstrap_register_all_engines(registry=engine_registry, verbose=False)
    agi = UnifiedAGISystem(engine_registry=engine_registry)
    
    report_file = "AGI_SCIENTIFIC_TEST_RESULTS_AR.md"
    
    with open(report_file, "w", encoding="utf-8") as f:
        f.write("# 🧪 تقرير اختبارات AGI العلمية الشاملة\n")
        f.write(f"**التاريخ:** {time.strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write("**الموديل:** Gemini 3 Pro (Preview) / Qwen2.5:7b-instruct\n\n")
        f.write("---\n\n")
        
        for i, exp in enumerate(EXPERIMENTS):
            print(f"\n🔬 Running {exp['title']}...")
            start_t = time.time()
            
            # Run Query with 120 second timeout per experiment
            try:
                result = await asyncio.wait_for(
                    agi.process_with_full_agi(exp['prompt']),
                    timeout=120.0  # 2 دقيقة كحد أقصى لكل تجربة
                )
                
                # Extract text
                answer_text = ""
                if isinstance(result, dict):
                    answer_text = result.get('final_response') or result.get('answer') or result.get('output') or str(result)
                else:
                    answer_text = str(result)
                    
            except asyncio.TimeoutError:
                answer_text = f"⏱️ Timeout: التجربة تجاوزت الحد الأقصى (120 ثانية)"
                print(f"   ⚠️ Timeout after 120s")
            except Exception as e:
                answer_text = f"❌ Error executing experiment: {str(e)}"
                
            duration = time.time() - start_t
            print(f"   ✅ Completed in {duration:.2f}s")
            
            # Write to report
            f.write(f"## {exp['level']}\n")
            f.write(f"### {exp['title']}\n\n")
            f.write(f"**⏱️ الوقت المستغرق:** {duration:.2f} ثانية\n\n")
            f.write("#### 📥 المدخلات (Prompt):\n")
            f.write(f"```text\n{exp['prompt']}\n```\n\n")
            f.write("#### 📤 المخرجات (AGI Output):\n")
            f.write(f"{answer_text}\n\n")
            f.write("---\n\n")
            f.flush() # Ensure write to disk
            
    print(f"\n📄 Report generated: {report_file}")

if __name__ == "__main__":
    asyncio.run(run_suite())
