from typing import Dict

SECTION_TEMPLATE = """\
أجب بصيغة تقرير منظّم بالعناوين التالية فقط:

1) تصميم نظام الري (10×20م | 1000$)

2) تنظيم المرور بتطبيق نفس مبادئ التدفق

3) التشابهات النظرية

4) خطوات تنفيذ عملية (مرقّمة)

5) تأمّل ذاتي وحدود النموذج
\n
مثال موجز للتوضيح (few-shot):
مثال: لنظام ري صغير، ذكرنا المضخة، التدفّق، والصمامات ثم طبّقنا مفهوم التدفّق على تقاطع مرور عبر ضبط الأولويات والتوقيت.
"""


def compose_prompt(task_text: str, intent: str = "analysis") -> str:
    # نعيد تشكيل السؤال: نلخّصه، نوضح المطلوب، ثم نفرض قالب الأقسام
    header = (
        "المهمة التالية قيد التحليل، أعد الإجابة بدقّة ووضوح وفق الأقسام الإلزامية.\n\n"
        f"نص المهمة:\n{task_text.strip()}\n\n"
        "تعليمات مهمة:\n"
        "- استخدم المصطلحات المطلوبة حرفيًا حيث يلزم.\n"
        "- اربط المبادئ بين المجالين بوضوح.\n"
        "- اختم بخطة تنفيذ مرقّمة وبند تأمّل ذاتي.\n\n"
    )
    return header + SECTION_TEMPLATE
